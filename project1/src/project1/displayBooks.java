    public void displayBooksInTable() {
        // Create the column names for the table
        String[] columnNames = {"ID", "Nama", "Stok"};

        // Create a DefaultTableModel with the column names
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        // Populate the model with book data from the bookList ArrayList
        for (Book book : bookList) {
            Object[] rowData = {book.getId(), book.getNama(), book.getStok()};
            model.addRow(rowData);
        }

        // Set the updated model to the jTable1
        jTable1.setModel(model);
    }
    
    public void connect() {
        try {
            // Establish a database connection
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_sip", "root", "");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    public void getBooks() {
        bookList = new ArrayList<>();
        try {
            // Prepare the SQL statement
            String query = "SELECT id, nama, stok FROM Book";
            PreparedStatement statement = connection.prepareStatement(query);

            // Execute the query
            ResultSet resultSet = statement.executeQuery();

            // Iterate over the result set and create Book objects
            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String nama = resultSet.getString("nama");
                int stok = resultSet.getInt("stok");
                Book book = new Book(id, nama, stok);
                bookList.add(book);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }


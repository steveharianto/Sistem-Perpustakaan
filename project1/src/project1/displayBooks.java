    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            btnDelete.setEnabled(false);
            btnUpdate.setEnabled(false);
        }else{
            btnDelete.setEnabled(true);
            btnUpdate.setEnabled(true);
            txtNama.setText(bookList.get(selectedRow).getNama());
            txtStok.setText(String.valueOf(bookList.get(selectedRow).getStok()));
        }
        selectedIndex = selectedRow;
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        bookList.get(selectedIndex).delete();
        JOptionPane.showMessageDialog(null, "Book deleted successfully");
        getBooks();
        displayBooksInTable();
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Book updatedBook = bookList.get(selectedIndex);
        updatedBook.setNama(txtNama.getText());
        updatedBook.setStok(Integer.valueOf(txtStok.getText()));
        updatedBook.update();
        JOptionPane.showMessageDialog(null, "Book Updated successfully");
        getBooks();
        displayBooksInTable();
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_btnUpdateActionPerformed

